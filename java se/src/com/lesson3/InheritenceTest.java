/**
 * 父类有的，子类也有；父类没有的，子类可以增加；父类有的，子类可以改变
 * @author Administrator
 *
 */
public class InheritenceTest {

	public static void main(String[] args) {
		Apple apple = new Apple();
		System.out.println(apple.name);
	}
}

class Fruit {
	String name = "fruit";
}

class Apple extends Fruit {
	String name = "apple";
}