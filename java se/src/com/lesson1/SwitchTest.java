package com.lesson1;

public class SwitchTest {

	public static void main(String[] args) {
		int a = 10;
		switch (a){
		case 1:
			System.out.println("1111");
			break;
		case 3:
			System.out.println("3333");
			break;
		case 5:
			System.out.println("5555");
			break;
		case 7:
			System.out.println("7777");
			break;
		case 10:
			System.out.println("10 10");
			break;
		default:
			System.out.println("0000");
		}
	}
}
