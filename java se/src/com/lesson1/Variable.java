package com.lesson1;

public class Variable {
	public static void main(String[] args) {
		double d = 1.2;
		System.out.println(d);
		
		float x = (float)d;
		System.out.println(x);
		System.out.println();
		
//		char a='A';
//		System.out.println(a);
		
		char c = 'c';
		System.out.println(c);
		
		float f = 1.2f;
		System.out.println(f);
		
//		boolean b = true;
//		System.out.println(b);
		
		short a = 1;
		int b = a;
		System.out.println(b);
	}
}
